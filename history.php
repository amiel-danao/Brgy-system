					Incidentally, Maragondon has three foundation dates; namely, 1. ) 1611 when the Franciscan Fathers from Silang established their first visita or chapel; 2.) 1690, the Fundacion Ecclesiastica or founding of the regular parish by the Jesuits, dedicating it to Our Lady of the Assumption; 3.) 1727, the Fundacion Civil, when the original barrio of Maragondon was separated from Silang during the administration of the Recollects and converted into an independent municipality with Gregorio Silvestre has he first gobernadorcillo. Maragondon belonged to the corregimiento of Mariveles (now Bataan province) until 1754 when Spanish gevernor General Pedro Manuel de Arandia (1754 A- 1759) abolished the politico A- military administration and restored Maragondon Cavite Province.</p>

