					<p>The name Maragondon was derived from Tagalog word madagundong or maugong, meaning much sound. Actually the sounds comes from a noisy river called Kay Albaran in the barrio of Capantayan. This area was the first townsite selected, but because the river frequently overflowed its bank and in undated the place it was later transferred to its present site. The word madagundong not being pleasant to ears, the people had it change to marigundong in honor of the town’ s patron saint, Nuestra Senora Maria Asuncion( Our Lady of The Assumption ).</p>
					
